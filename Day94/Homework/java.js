//while loops

console.log("While Loop task 1: Numbers from 0 to 20, incrementing by 2");
let num1 = 0;
while (num1 <= 20) {
  console.log(num1);
  num1 += 2;
}

console.log("while Loop task 2: Countdown from 10 to 1");
let num2 = 10;
while (num2 >= 1) {
  console.log(num2);
  num2--;
}

console.log("While Loop task 3: Count numbers from 1 to 5");
let count = 0;
let num3 = 1;
while (num3 <= 5) {
  count++;
  num3++;
}
console.log("The count of numbers from 1 to 5 is:", count);

console.log("While Loop task 4: Multiply by 2 until greater than 100");
let num4 = 1;
