#1. შექმენით ფუნქცია, რომელსაც გადაცემთ 2 არგუმენტს (რიცხვს), შემდეგ ეს 2 არგუმენტი დაუმატეთ ერთმანეთს და დაპრინტეთ.

def add_numbers(a, b):
    result = a + b
    print(f"The sum of {a} and {b} is {result}") 

#2. შექმენით ფუნქცია, რომელსაც გადაცემთ 2 არგუმენტს (რიცხვს), შემდეგ ეს 2 არგუმენტი გაამრავლეთ ერთმანეთზე და დაპრინტეთ.

def multiply_numbers(a, b):
    result = a * b
    print(f"The product of {a} and {b} is {result}")

add_numbers(3, 5)   
multiply_numbers(4, 6)   