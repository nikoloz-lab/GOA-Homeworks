
var1 = "Hello"  # Variable 1, storing a string with 5 characters
var2 = "Python Programming"  # Variable 2, storing a string with 18 characters
var3 = "Assistent"  # Variable 3, storing a string with 9 characters

# Count the number of characters in each variable using the len() function
# len() is a built-in function that returns the number of items in an object (in this case, characters in a string)
char_count_var1 = len(var1)  # Counts characters in var1
char_count_var2 = len(var2)  # Counts characters in var2
char_count_var3 = len(var3)  # Counts characters in var3

# Print the results
print(f"Characters in var1: {char_count_var1}")  # Outputs: 5
print(f"Characters in var2: {char_count_var2}")  # Outputs: 18
print(f"Characters in var3: {char_count_var3}")  # Outputs: 9