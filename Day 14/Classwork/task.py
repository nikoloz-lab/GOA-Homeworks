# for loop-ის მეშვეობით დაპრინტეთ რიცხვებო: 0,1,2,3.

for i in range(4):
    print(i)

# for loop-ის მეშვეობითად დაპრინტეთ 'hello world' 5-ჯერ.

for _ in range(5):
    print("hello world")

